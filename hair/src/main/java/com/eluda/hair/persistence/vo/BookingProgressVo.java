package com.eluda.hair.persistence.vo;

public class BookingProgressVo {
	private int bookingProgressId;
	private String languageId;
	private String name;
	
	public int getBookingProgressId() {
		return bookingProgressId;
	}
	public void setBookingProgressId(int bookingProgressId) {
		this.bookingProgressId = bookingProgressId;
	}
	public String getLanguageId() {
		return languageId;
	}
	public void setLanguageId(String languageId) {
		this.languageId = languageId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}	
}
